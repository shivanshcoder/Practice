#include <stdio.h>
int main()
{

	printf("Hello\nWorld!");    //use of \n

	printf("\nHello\\World!");    //use of \\
									
	printf("\nHello\'W\'orld!");    //use of \'

	printf("\nHello\tWorld!");  // use of \t

	printf("\n\"Hello World!\"");   //use of \"

	printf("\nHello\bWorld!");      //use of \b

	printf("\nHello W\rorld!\n");      //use of \r
	system("pause");
	return 0;
}